package net.mamoe.mirai.plugincenter.model;

import javax.persistence.*;
import java.util.Objects;

@Entity
@Table(name = "user_role", schema = "public", catalog = "plugins")
public class UserRoleEntity {
    private int id;
    private int uid;
    private int roleId;

    @Id
    @Column(name = "id")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "uid")
    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    @Basic
    @Column(name = "role_id")
    public int getRoleId() {
        return roleId;
    }

    public void setRoleId(int roleId) {
        this.roleId = roleId;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        UserRoleEntity that = (UserRoleEntity) o;
        return id == that.id && uid == that.uid && roleId == that.roleId;
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, uid, roleId);
    }
}
